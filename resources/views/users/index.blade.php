@push('myscript')
    <script>
        $(function() {
            $("#btnTambahuser").click(function() {
                $("#modal-inputuser").modal("show");
            });

            $(".edit").click(function() {
                var id_user = $(this).attr('id_user');
                $.ajax({
                    type: 'POST',
                    url: '/konfigurasi/users/edit',
                    cache: false,
                    data: {
                        _token: "{{ csrf_token() }}",
                        id_user: id_user
                    },
                    success: function(respond) {
                        $("#loadeditform").html(respond);
                    }
                });

                $("#modal-edituser").modal("show");
            });

            $(".delete-confirm").click(function(e) {
                var form = $(this).closest('form');
                e.preventDefault();
                Swal.fire({
                    title: "Apakah Anda Yakin?",
                    text: "Data Anda Akan Dihapus!",
                    icon: "warning",
                    showCancelButton: true,
                    confirmButtonColor: "#3085d6",
                    cancelButtonColor: "#d33",
                    confirmButtonText: "Yes, delete it!"
                }).then((result) => {
                    if (result.isConfirmed) {
                        form.submit();
                        Swal.fire({
                            title: "Deleted!",
                            text: "Data Berhasil Dihapus.",
                            icon: "success"
                        });
                    }
                });
            });

            $("#formuser").submit(function() {
                var nama_user = $("#nama_user").val();
                var email = $("#email").val();
                var role = $("#role").val();

                if (nama_user == "") {
                    Swal.fire({
                        title: 'Warning!',
                        text: 'Nama User Harus Diisi!',
                        icon: 'warning',
                        confirmButtonText: 'Ok'
                    }).then((result) => {
                        $("#nama_user").focus();
                    });
                    return false;
                } else if (email == "") {
                    Swal.fire({
                        title: 'Warning!',
                        text: 'Email User Harus Diisi!',
                        icon: 'warning',
                        confirmButtonText: 'Ok'
                    }).then((result) => {
                        $("#email").focus();
                    });
                    return false;
                } else if (role == "") {
                    Swal.fire({
                        title: 'Warning!',
                        text: 'Role Harus Dipilih',
                        icon: 'warning',
                        confirmButtonText: 'Ok'
                    }).then((result) => {
                        $("#role").focus();
                    });
                    return false;
                }
            });
        });
    </script>
